// makeCanvas.js

function makeCanvas()
{
    let theCanvas = ce('canvas');
    theCanvas.id = 'map';
    theCanvas.style.width = '850px';
    theCanvas.style.height = '400px';
    theCanvas.style.position = 'absolute';
    theCanvas.style.left = 0 + 'px';
    theCanvas.style.top = 17 + 'px';
    ba(theCanvas);
}

